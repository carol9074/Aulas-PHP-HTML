    <h1><strong> Corpo encontrado na orla do Guaíba em Porto Alegre é de jovem que desapareceu há 4 dias, diz polícia;</strong></h1>
        <h3>Corpo foi localizado na manhã desta quarta-feira (27). Polícia Civil trabalha para esclarecer o que aconteceu.</h3>

            <p><h2><em>A Polícia Civil confirmou que <u> a pessoa encontrada morta na orla do Guaíba</u>, em <u>Porto Alegre</u>, na manhã desta quarta-feira (27), é um jovem de 27 anos que desapareceu no último sábado (23) – há quatro dias. O nome dele é Florismar Gomes.
                <br><br>
                De acordo com o delegado Thiago Lacerda, responsável pela investigação, há duas hipóteses para o que pode ter acontecido: homicídio ou suicídio.
            <br><br>
            "Segundo a família, que reside em Goiás, Florismar saía muito para encontros marcados via aplicativo. Então, pode ter ocorrido um homicídio por questão patrimonial, por exemplo. Como o corpo não tinha nenhuma lesão aparente, suicídio é uma possibilidade que não pode ser descartada", explica o delegado Lacerda.</em>
            </h2>
